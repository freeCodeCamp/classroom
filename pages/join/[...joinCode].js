import Head from 'next/head';
import Navbar from '../../components/navbar';
import { useState } from 'react';
import { useRouter } from 'next/router';
import { getSession } from 'next-auth/react';
import AuthButton from '../../components/authButton';

export async function getServerSideProps(ctx) {
  const userSession = await getSession(ctx);
  return {
    props: {
      userSession: userSession
    }
  };
}
export default function JoinWithCode({ userSession }) {
  const [formData] = useState({});
  const router = useRouter();
  const { joinCode } = router.query;

  const classroomRequest = async event => {
    event.preventDefault();
    formData.join = joinCode;
    try {
      const res = await fetch(`/api/student_email_join`, {
        method: 'PUT',
        headers: {
          'Content-Type': 'application/json'
        },
        body: JSON.stringify(formData)
      });
      if (res.status === 409) {
        alert('You have already joined this classroom.');
      } else {
        alert('Congrats! You are now enrolled in this class.');
      }
    } catch (error) {
      alert('Sorry, there was an error on our end. Please try again later.');
      console.log(error);
    }
  };

  return (
    <>
      <div>
        <Head>
          <title>Create Next App</title>
          <meta name='description' content='Generated by create next app' />
          <link rel='icon' href='/favicon.ico' />
        </Head>
        <Navbar />
        {userSession ? (
          <>
            <div className='min-h-full flex items-center justify-center py-40 px-4 sm:px-6 lg:px-8'>
              <div className='max-w-md w-full space-y-8'>
                <div>
                  <h2 className='mt-6 text-center text-3xl font-extrabold text-gray-900'>
                    Register for Classroom
                  </h2>
                </div>
                <form className='mt-8 space-y-6' onSubmit={classroomRequest}>
                  <div>
                    <button
                      type='submit'
                      className='group relative w-full flex justify-center py-2 px-4 border border-[#0a0a23] text-sm font-medium rounded-md text-black bg-[#d0d0d5] hover:bg-[#0a0a23] focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500'
                    >
                      <span className='absolute left-0 inset-y-0 flex items-center pl-3'>
                        {/* <!-- Heroicon name: solid/lock-closed --> */}
                        <svg
                          className='h-5 w-5 text-[#0a0a23] group-hover:text-white'
                          xmlns='http://www.w3.org/2000/svg'
                          viewBox='0 0 20 20'
                          fill='currentColor'
                          aria-hidden='true'
                        >
                          <path
                            fillRule='evenodd'
                            d='M5 9V7a5 5 0 0110 0v2a2 2 0 012 2v5a2 2 0 01-2 2H5a2 2 0 01-2-2v-5a2 2 0 012-2zm8-2v2H7V7a3 3 0 016 0z'
                            clipRule='evenodd'
                          />
                        </svg>
                      </span>
                      <span className='text-black group-hover:text-white'>
                        Submit Reqest
                      </span>
                    </button>
                  </div>
                </form>
              </div>
            </div>
          </>
        ) : (
          <>
            <div className='min-h-full flex items-center justify-center py-40 px-4 sm:px-6 lg:px-8'>
              <div className='max-w-md w-full space-y-8'>
                <div>
                  <h2 className='mt-6 text-center text-3xl font-extrabold text-gray-900'>
                    Sign In with FreeCodeCamp
                  </h2>
                </div>
                <div className='pl-2 min-h-full flex items-center justify-center'>
                  <AuthButton></AuthButton>
                </div>
              </div>
            </div>
          </>
        )}
      </div>
    </>
  );
}
