import Head from 'next/head';
import Layout from '../../components/layout';
import Link from 'next/link';
import Navbar from '../../components/navbar';
import DashTabs from '../../components/dashtabs';
import { getSession } from 'next-auth/react';
import { createSuperblockDashboardObject } from '../../util/dashboard/createSuperblockDashboardObject';
import { fetchStudentData } from '../../util/student/fetchStudentData';
import redirectUser from '../../util/redirectUser.js';

import {
  getAvailableSuperblocks,
  getSuperblocksByIndices
} from '../../util/curriculum/fetchCurriculum';

export async function getServerSideProps(context) {
  // Dynamic import to prevent Prisma from being bundled for client
  const { default: prisma } = await import('../../prisma/prisma');

  //making sure User is the teacher of this classsroom's dashboard
  const userSession = await getSession(context);
  if (!userSession) {
    return redirectUser('/error');
  }
  const userEmail = await prisma.User.findMany({
    where: {
      email: userSession['user']['email']
    }
  });

  const classroomTeacherId = await prisma.classroom.findUnique({
    where: {
      classroomId: context.params.id
    },
    select: {
      classroomTeacherId: true
    }
  });

  if (userEmail[0].id !== classroomTeacherId['classroomTeacherId']) {
    return redirectUser('/classes');
  }

  const certificationNumbers = await prisma.classroom.findUnique({
    where: {
      classroomId: context.params.id
    },
    select: {
      fccCertifications: true
    }
  });
  const allSuperblocks = await getAvailableSuperblocks();
  let nonDashedNames = certificationNumbers.fccCertifications.map(
    i => allSuperblocks[i]?.title
  );

  let superBlockJsons = await getSuperblocksByIndices(
    certificationNumbers.fccCertifications
  );
  let dashboardObjs = await createSuperblockDashboardObject(superBlockJsons);

  let currStudentData = await fetchStudentData();

  return {
    props: {
      userSession,
      columns: dashboardObjs,
      certificationNames: nonDashedNames,
      data: currStudentData
    }
  };
}

export default function Home({
  userSession,
  columns,
  certificationNames,
  data
}) {
  let tabNames = certificationNames;
  let columnNames = columns;
  let studentData = data;

  return (
    <Layout>
      <Head>
        <title>Create Next App</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      {userSession && (
        <>
          <Navbar>
            <div className='border-solid border-2 pl-4 pr-4'>
              <Link href={'/classes'}>Classes</Link>
            </div>
            <div className='border-solid border-2 pl-4 pr-4'>
              <Link href={'/'}> Menu</Link>
            </div>
          </Navbar>
          <DashTabs
            columns={columnNames}
            certificationNames={tabNames}
            studentData={studentData}
          ></DashTabs>
        </>
      )}
    </Layout>
  );
}
